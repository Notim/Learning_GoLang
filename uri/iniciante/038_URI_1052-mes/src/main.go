package main

import io "fmt"

func main() {
	var input int

	io.Scanf("%d", &input)

	switch input {
		case 1:
			io.Println("January")

		case 2:
			io.Println("February")

		case 3:
			io.Println("March")

		case 4:
			io.Println("April")

		case 5:
			io.Println("May")

		case 6:
			io.Println("June")

		case 7:
			io.Println("July")

		case 8:
			io.Println("August")

		case 9:
			io.Println("September")

		case 10:
			io.Println("October")

		case 11:
			io.Println("November")

		case 12:
			io.Println("December")

	}
}
